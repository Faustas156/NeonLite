        static void Activate(bool activate)
        {
            if (activate)
                randist = new System.Random((int)DateTime.Now.ToBinary());

            Patching.TogglePatch(activate, typeof(NoMission), "PreCreateButton", DontBlock, Patching.PatchTarget.Prefix);
            Patching.TogglePatch(activate, typeof(LeaderboardScore), "SetScore", LBSetScore, Patching.PatchTarget.Postfix);
            Patching.TogglePatch(activate, typeof(MenuScreenLocation), "CreateActionButton", IHidInGarbage, Patching.PatchTarget.Postfix);
            Patching.TogglePatch(activate, typeof(LocalizationManager), "GetTranslation", FunkyLocale, Patching.PatchTarget.Prefix);
            Patching.TogglePatch(activate, typeof(MainMenu), "SelectLevel", OopsWrongLevel, Patching.PatchTarget.Prefix);
        }


        static bool IsToday()
        {
            var now = DateTime.Now;
            return (now.Month == 4 && now.Day == 1) || force;
        }
        static bool Chance(double chance)
        {
            if (!IsToday())
                return false;

            var d = randist.NextDouble();
            NeonLite.Logger.DebugMsg($"{d} / {chance}");
            return d <= chance;
        }

        static bool DontBlock(ref bool __result)
        {
            if (Chance(0.15))
            {
                __result = true;
                return false;
            }
            return true;
        }

        static void LBSetScore(LeaderboardScore __instance, ref ScoreData newData)
        {
            if (!newData._userScore || !IsToday())
                return;

            __instance._ranking.color = Color.red;
            __instance._username.color = Color.red;
            __instance._scoreValue.color = Color.red;
        }

        static void IHidInGarbage(HubAction hubAction, MenuButtonHolder __result)
        {
            if (hubAction.ID == "main.mission12_hub_neon" && Chance(0.5))
                __result.transform.SetAsFirstSibling();
        }

        static bool FunkyLocale(string Term, string overrideLanguage, object[] __args, ref string __result)
        {
            if (overrideLanguage == "English")
                return true;

            const string HEADER = "NLAF/";
            if (string.IsNullOrWhiteSpace(Term) || Term.StartsWith(HEADER))
                return true;

            NeonLite.Logger.DebugMsg(HEADER + Term);

            if (Chance(0.1) && LocalizationManager.TryGetTranslation(HEADER + Term, out var _))
            {
                __args[0] = HEADER + Term;
                __result = (string)Helpers.Method(typeof(LocalizationManager), "GetTranslation").Invoke(null, __args);
                return false;
            }
            return true;
        }

        static bool OopsWrongLevel(MainMenu __instance, string levelID, bool fromArchive)
        {
            if (!fromArchive)
                return true;

            var gd = NeonLite.Game.GetGameData();
            var lv = gd.GetLevelData(levelID);
            var mission = gd.campaigns.SelectMany(x => x.missionData).First(x => x.levels.Contains(lv));

            if (Chance(0.01) && mission.levels.First() != lv)
                levelID = mission.levels[mission.levels.IndexOf(lv) - 1].levelID;
            else if (Chance(0.01) && mission.levels.Last() != lv)
                levelID = mission.levels[mission.levels.IndexOf(lv) + 1].levelID;
            else if (Chance(0.001))
            {
                // unlucky
                var list = gd.GetLevelDataIDsList(false);
                levelID = list[randist.Next(0, list.Count)];
            }

            RM.acceptInputPauseMenu = true;
            __instance.PauseGameNoStateChange(setPause: false);
            __instance.OnHideRelationshipStatus();
            __instance._screenLocation.StopFade();
            Singleton<Game>.Instance.PlayLevel(levelID, true);
            return false;
        }
